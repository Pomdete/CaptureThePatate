# Exemple de reconstruction de input2 original à partir des octets modifiés par l’exception
modded = [
        0x39, 0x03, 0xb2, 0xef, 0xf8, 0x0e, 0x71, 0xb4,
        0xdb, 0x93, 0xf6, 0x51, 0x3a, 0x62, 0xa6, 0xe8,
        0x64, 0x9b, 0x81, 0x04, 0x01, 0x42, 0x74, 0xb5
]  # dump post-breakpoint (array[24..47])

frame = [
    0x05, 0x00, 0xA0, 0x00,  # PS
    0x3C, 0x00, 0x00, 0x40,  # A0 (return addr)
    0x78, 0x56, 0x34, 0x12,  # A1
    0xEF, 0xCD, 0xAB, 0x90,  # A2
    0x00, 0x11, 0x22, 0x33,  # A3
    0x44, 0x55, 0x66, 0x77,  # A4
    0x88, 0x99, 0xAA, 0xBB   # A5 (exemple)
]  # octets poussés sur la stack par l’exception entry

orig = [m ^ f for m, f in zip(modded, frame)]
print(bytes(orig))
